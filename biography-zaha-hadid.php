       <article>

		<p>  <strong>Заха Хадід</strong> (1950-2016) народилася в Багдаді, Ірак, і почала навчання в Американському університеті в Бейруті в галузі математики. У 1972 році переїхала до Лондона, щоб вивчати архітектуру в Архітектурній асоціації, а після закінчення навчання в 1977 році приєдналася до офісу міської архітектури (OMA). Вона також викладала в Архітектурній асоціації (AA) разом із співробітниками OMA Ремом Колхаасом та Елією Зенгелісом.
		Заха Хадід розпочала власну практику в Лондоні в 1980 році та виграла престижний конкурс Hong Kong Peak Club, центру відпочинку та відпочинку в 1983 році. Живопис і малюнок, особливо в її ранній період, є важливими методами дослідження для її дизайнерської роботи. З моменту її ретроспективної виставки 1983 року в АА у Лондоні її архітектура була представлена на виставках по всьому світу, а багато її робіт зберігаються у важливих музейних колекціях.</p>
		<p>Відома як архітектор, який постійно розширює межі архітектури та міського дизайну, її робота експериментує з новими просторовими концепціями, посилюючи існуючі міські ландшафти та охоплюючи всі сфери дизайну, від міського масштабу до інтер’єрів та меблів.</p>

