       	<h2>Історія</h2>    	

		<p>Архітектура епохи Відродження розвивалася як частина відродження класицизму у Флоренції, Італія, приблизно в 1400 році. Вона розвивалася протягом наступних 200 років, поширюючись по всій Італії, а потім і Європі. Архітектори епохи Відродження в Італії черпали натхнення в стародавніх греко-римських руїнах і ранніх спорудах, таких як Пантеон і Колізей у Римі, а також у роботах римського архітектора Марка Вітрувія Полліона (80 р. до н. е.-15 р. до н. е.), які були опубліковані в 1486 р. 
		</p>
		<p>Архітектори епохи Відродження не просто хотіли відтворити минуле. Вони прагнули використати класичні елементи для інноваційних структур, які сягають корінням в історію, але адаптуються до сучасного світу та розвитку міст.
		</p>
		<p>Архітектуру епохи Відродження зазвичай поділяють на три основні періоди. Це почалося з Раннього Відродження, яке почалося приблизно в 1400 році, коли архітектори шукали натхнення в античності. Вони знову запровадили в будівлі класичні римські та грецькі елементи, такі як арки, колони та куполи. Будинки раннього Відродження мали симетричні фасади та чіткі, обтічні об’єми, які відзначали зміну складніших готичних пропорцій, які їм передували.
     
     	Починаючи приблизно з 1500 року, Високе Відродження було періодом, коли використання класичних елементів, адаптованих до сучасних будівельних стилів 16-го століття, було в повному розквіті. Потім, під час Пізнього Відродження, починаючи приблизно з 1520 року (також званого маньєризмом), використання декоративних і декоративних класичних елементів, таких як куполи та куполи, стало більш поширеним.     	
     </p>

