       <h2>Характерні риси</h2>       	 
	
		<ul class="ul-definition">
			<li> Симетрія і пропорція. Архітектура епохи Відродження орієнтувалася на класичні уявлення про красу, засновані на пропорції та симетрії. Він також включав геометрію, і багато планів будівель були симетричними квадратами</li>
			<li> Цегляна кладка. Зовнішній вигляд, як правило, представляв собою тесану кладку, стиль кладки, при якому каміння рівномірно розрізано в квадраті чи прямокутнику, а потім укладається горизонтально з мінімальною кількістю розчину.</li>
			<li> Класичні елементи. Архітектура епохи Відродження використовувала багато класичних елементів, включаючи куполи, колони, пілястри (прямокутні колони), перемички (тип балки), арки та фронтони (трикутні фронтони) у впорядкованому та повторюваному порядку.</li>
			<li> Повітря і Світло. Будівлі раннього Відродження зосереджувалися на привнесенні повітря та світла в простір. Це був реверанс до зародження ідеалів і думки Відродження.</li>			
		</ul>
