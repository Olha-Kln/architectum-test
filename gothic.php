       	<h2>Історія</h2>     	

		<p>  Готична архітектура виникла у Франції, де вона спочатку називалася «Opus Francigenum», або «французька робота». Починаючи з середини XII століття, прогрес у будівельній інженерії дозволив французьким будівельникам створювати масивні споруди. Деякі з ключових елементів готичної архітектури, як-от великі вітражі та склепінчасті арки, пропускали достатню кількість природного світла в конструкції, незважаючи на їхні величезні розміри та висоту. Цей стиль домінував у європейській архітектурі, особливо в будівлях, побудованих римсько-католицькою церквою, аж до 16 століття, коли він став відомий як готична архітектура.</p>
		<p>
		З часом готична архітектура розвивалася в кілька етапів:
		<ol>
		<li>Роки високої готики, які тривали з 1250 по 1300 рр. і вперше були представлені в Шартрському соборі у Франції. Цей період часу визначався стилем Rayonnant або дуже вишуканим декором на зовнішніх будинках. Протягом наступних кількох десятиліть Німеччина, Іспанія та Великобританія створили власні варіації готичної архітектури Rayonnant.</li>
		<li>Пізня готика, яка тривала з 15 по 16 ст. Протягом цього часу Німеччина виробляла надзвичайно високі церкви зі склепінчастими стелями. У цей час також були популярні британська перпендикулярна готична архітектура (яка визначалася акцентом на вертикальних лініях) і французький стиль Flamboyant (надзвичайно складне зовнішнє оздоблення).</li>		
	</ol></p>
	

