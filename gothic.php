       	<h2>Характерні риси</h2> 
<p>
	<ul class="ul-definition">
		<li>Вітражі.
		Вітражі часто зустрічаються в місцях відправлення культу, але вони надзвичайно поширені в готичних церквах і соборах. Вікна, як правило, дуже високі та аркові, або круглі, і мали на меті пропускати якомога більше природного світла. У готичних вітражах часто можна зустріти візерунок, декоративну кам’яну підставку, а також біблійні сцени.</li>
		<li>Стрілчасті арки.
		Замість того, щоб використовувати круглі романські арки, готичні будівельники будували високі тонкі загострені арки. Натхненні ісламськими архітектурними стилями готичні стрілчасті арки підкреслювали висоту стелі, вміщували склепінчасті стелі та символічно вказували на небо. Ви також знайдете ці типи арок в ісламській архітектурі.</li>
		<li>Ребристі склепіння.
		Щоб побудувати надзвичайно високі споруди з високими вікнами, готичні архітектори почали використовувати ребристі склепіння — або аркові склепіння, розташовані паралельно одне одному, щоб підтримувати округлий дах — замість традиційних склепінчастих балок. Ці склепіння, що перетинаються, не тільки створювали візуальний інтерес, але й забезпечували більшу підтримку високих готичних споруд.</li>
		<li>Літаючі контрфорси.
		Літаючі контрфорси були ще одним досягненням готичної епохи, яке допомогло підтримувати високі, важкі готичні будівлі. Літаючі контрфорси мають форму половини арки та забезпечують підтримку, перерозподіляючи вагу з вищого, важкого рівня, на нижчий, міцніший рівень.
		<li>Декоративні елементи.
		Для готичної архітектури характерне надзвичайно пишне зовнішнє оздоблення. Прикрашені колони, ліпнина, статуї, вершини, шпилі та горгульї, з яких б’є вода, зазвичай зустрічаються в готичній архітектурі.</li>
	</ul>
	</p>
